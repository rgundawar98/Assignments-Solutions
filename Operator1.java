package Operators;

public class Operator1 
{ 
   public static void Operators()
   {
	   int x = 10;
	   int y = 20;
	   int sum , sub , mul , div;
	   
	   sum = x + y;
	   sub = x - y;
	   mul = x * y;
	   div = x/y;
	   
	   
	   System.out.println(sum);
	   System.out.println(sub);
	   System.out.println(mul);
	   System.out.println(div);
	   
   }
	public static void main(String[] args) 
	{
		Operators();
	}
	 
 }


